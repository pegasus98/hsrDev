static void init(void)
{
  int ttl;

  sock_fd = socket(AF_INET, SOCK_DGRAM, 0);
  if (sock_fd < 0) {
    perror("socket");
    exit(-1);
  }

  ttl = 64;
  if (setsockopt(sock_fd, IPPROTO_IP, IP_TTL, &ttl, sizeof(ttl)) < 0) {
    perror("setsockopt");
    exit(-1);
  }
}

static uint64_t timestamp(void)
{
  struct timespec tms;

  if (clock_gettime(CLOCK_REALTIME, &tms) < 0) {
    perror("clock_gettime");
    exit(-1);
  }

  return tms.tv_sec * 1000000000ull + tms.tv_nsec;
}

static void work(const char *server_ip, unsigned long n)
{
  struct sockaddr_in dst_addr;
  unsigned long t;

  dst_addr.sin_family = AF_INET;
  dst_addr.sin_addr.s_addr = inet_addr(server_ip);
  dst_addr.sin_port = htons(DST_PORT);

  for (t = 0; t < n; ++t)
  {
    size_t len = randseq[t & 511];
    for (size_t j = 0; j < len; ++j)
        buf[j] = rand();

    ssize_t ret = sendto(sock_fd, buf, len, 0,
                         (struct sockaddr *) &dst_addr,
                         sizeof(dst_addr));
    if (ret != len) {
      perror("sendto");
      exit(-1);
    }
    printf("send %luth packet (len=%lu, timestamp=%llu)...\n",
            t, (unsigned long) len, (unsigned long long) timestamp());
    fflush(stdout);

    usleep(1000 * 40 + rand() % (1000 * 20));
  }
}

int main(int argc, char **argv)
{
  if (argc != 3) {
    fprintf(stderr, "Usage: %s [SERVER IP] [PKT NUM]\n", argv[0]);
    return 1;
  }

  init();
  work(argv[1], strtoul(argv[2], NULL, 10));

  return 0;
}

